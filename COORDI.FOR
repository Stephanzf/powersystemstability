	SUBROUTINE ENGINE(SWII)        
	
	INTEGER SWII

	IF (SWII.EQ.0) THEN
	
	NT     = 0
	TIME   = 0.0
	TFIN   = 0.0
	NSTEP  = 0
	NPRINT = 0

6       CALL    READIN
	
	SWII = -99
	RETURN
	ENDIF

	IF (SWII.EQ. -99) THEN
	CALL APPARATUSTHREE
	SWII = -1
	RETURN
	ENDIF

	IF (SWII.EQ. -1) THEN
	CALL APPARATUSONE
	SWII = 99
	RETURN
	ENDIF

	IF (SWII.EQ. 99) THEN
	CALL APPARATUSTWO
	RETURN
	ENDIF

	END



